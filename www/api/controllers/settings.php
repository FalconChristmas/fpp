/**
 * Reads and returns the parsed settings.json metadata file.
 *
 * @return array Associative array of settings metadata.
 */
function ReadSettingsJSON()
{
    global $settings;
    $json = file_get_contents($settings['fppDir'] . '/www/settings.json');

    return json_decode($json, true);
}

/**
 * Get value of setting
 *
 * Get info about a particular setting, including its current `value`.
 *
 * @route GET /api/settings/{SettingName}
 * @response 200 Setting metadata and current value
 * ```json
 * {
 *   "name": "AudioFormat",
 *   "description": "Audio Output Format",
 *   "tip": "The Audio Format generated by the decoder",
 *   "level": 1,
 *   "restart": 2,
 *   "default": 0,
 *   "type": "select",
 *   "options": {
 *     "Default": 0,
 *     "MP3": 1,
 *     "Ogg": 2,
 *     "Flac": 3
 *   }
 * }
 * ```
 */
function GetSetting()
{
    global $settings;

    $sInfos = ReadSettingsJSON();
    $settingName = params('SettingName');

    if (isset($sInfos['settings'][$settingName])) {
        $sInfo = $sInfos['settings'][$settingName];
    } else {
        $sInfo = array();
    }

    if (isset($settings[$settingName])) {
        $sInfo['value'] = $settings[$settingName];
    }

    if (isset($sInfo['optionsURL'])) {
        $json = "";
        if (preg_match('/^\//', $sInfo['optionsURL'])) {
            $json = file_get_contents($sInfo['optionsURL']);
        } else {
            $json = file_get_contents("http://" . $_SERVER['SERVER_ADDR'] . "/" . $sInfo['optionsURL']);
        }

        $sInfo['options'] = json_decode($json, true);
    }

    return json($sInfo);
}

/**
 * Set value for setting
 *
 * Sets the value for a specific setting.
 *
 * @route PUT /api/settings/{SettingName}
 * @body "0"
 * @response 200 Setting saved
 * ```json
 * {"status": "OK"}
 * ```
 */
function PutSetting()
{
    global $SUDO, $settings;
    $value = file_get_contents('php://input');
    $setting = params('SettingName');

    WriteSettingToFile($setting, $value);

    if (str_starts_with($setting, "LogLevel")) {
        SendCommand("LogLevel,$setting,$value,");
    } else if ($setting == "HostName") {
        $value = preg_replace("/[^-a-zA-Z0-9]/", "", $value);
        exec(
            $SUDO . " sed -i 's/^.*\$/$value/' /etc/hostname ; " .
            $SUDO . " sed -i '/^127.0.1.1[^0-9]/d' /etc/hosts ; " .
            $SUDO . " sed -i '\$a127.0.1.1 $value' /etc/hosts " .
            $SUDO . " hostname $value ; ",
            $output,
            $return_val
        );
    } else if ($setting == "EnableRouting") {
        if ($value != "1") {
            $value = "0";
        }
        exec(
            $SUDO . " sed -i '/net.ipv4.ip_forward/d' /etc/sysctl.conf; " .
            $SUDO . " sed -i '\$anet.ipv4.ip_forward = $value' /etc/sysctl.conf ; " .
            $SUDO . " sysctl --system",
            $output,
            $return_val
        );
    } else if ($setting == "storageDevice") {
        if ($settings['BeaglePlatform']) {
            exec('findmnt -n -o SOURCE / | colrm 1 5', $output, $return_val);
            $rootDevice = $output[0];
            unset($output);
        } else {
            exec('lsblk -l | grep " /$" | cut -f1 -d" "', $output, $return_val);
            $rootDevice = $output[0];
            unset($output);
        }
        if ($value == "--none--") {
            $value = $rootDevice;
        } else {
            $fsckOrder = "0";
            exec($SUDO . " file -sL /dev/$value | grep FAT", $output, $return_val);
            if ($output[0] == "") {
                unset($output);
                exec($SUDO . " file -sL /dev/$value | grep BTRFS", $output, $return_val);

                if ($output[0] == "") {
                    unset($output);
                    exec($SUDO . " file -sL /dev/$value | grep DOS", $output, $return_val);
                    if ($output[0] == "") {
                        # probably ext4
                        $options = "defaults,noatime,nodiratime,nofail";
                        $fsckOrder = "2";
                    } else {
                        # exFAT probably
                        $options = "defaults,noatime,nodiratime,exec,nofail,uid=500,gid=500";
                        $fsckOrder = "2";
                    }
                } else {
                    # BTRFS, turn on compression since fseq files are very compressible
                    $options = "defaults,noatime,nodiratime,compress=zstd,nofail";
                    $fsckOrder = "0";
                }
            } else {
                # FAT filesystem
                $options = "defaults,noatime,nodiratime,exec,nofail,flush,uid=500,gid=500";
                $fsckOrder = "2";
            }
        }
        if (preg_match("/$rootDevice/", $value)) {
            exec($SUDO . " sed -i 's/.*home\/fpp\/media/#\/dev\/sda1    \/home\/fpp\/media/' /etc/fstab", $output, $return_val);
        } else {
            exec($SUDO . " sed -i 's/.*home\/fpp\/media.*/\/dev\/$value	\/home\/fpp\/media	auto	$options	0	$fsckOrder /' /etc/fstab", $output, $return_val);
        }
        unset($output);
    } else if ($setting == "AudioOutput") {
        SetAudioOutput($value);
        // In Simple PipeWire mode, the AudioOutput selection drives the
        // synthesised PipeWire audio group config — re-apply it now.
        $settings[$setting] = $value;
        if (IsSimplePipeWireBackend($settings)) {
            ob_start();
            ApplyPipeWireSimpleConfig();
            ob_end_clean();
        }
    } else if ($setting == "VideoOutput") {
        ApplySetting($setting, $value);
        SendCommand("SetSetting,$setting,$value,");
        // In Simple PipeWire mode, the VideoOutput selection drives the
        // synthesised PipeWire video group config — re-apply it now.
        $settings[$setting] = $value;
        if (IsSimplePipeWireBackend($settings)) {
            ob_start();
            ApplyPipeWireSimpleConfig();
            ob_end_clean();
        }
    } else if ($setting == "MediaBackend") {
        ApplySetting($setting, $value);
        SendCommand("SetSetting,$setting,$value,");
        exec($SUDO . " " . $settings['fppDir'] . "/src/fppinit setupAudio", $output, $return_val);
        unset($output);
        // When switching INTO Simple PipeWire mode, generate and apply the
        // single-card / single-display config from the existing AudioOutput
        // / VideoOutput selections.  Advanced-mode JSON files are left
        // untouched so users can switch back without losing custom configs.
        $settings[$setting] = $value;
        if (IsSimplePipeWireBackend($settings)) {
            ob_start();
            ApplyPipeWireSimpleConfig();
            ob_end_clean();
        } else if (IsPipeWireBackend($settings)) {
            // Switching INTO Advanced PipeWire mode: re-apply the saved
            // advanced audio/input-group config so the regenerated graph
            // and PipeWireSinkName match.  Without this, PipeWireSinkName
            // is left at whatever the previous mode set (e.g. the Simple
            // mode "fpp_group_default"), which does not exist in the
            // advanced graph — fppd's stream is then orphaned and silent.
            // Audio groups first (output sinks), then input groups (mix
            // buses target the output groups and write the final
            // PipeWireSinkName, e.g. fpp_input_mix_bus_1).
            ob_start();
            ApplyPipeWireAudioGroups();
            ApplyPipeWireInputGroups();
            ob_end_clean();
        }
    } else if ($setting == "EnableTethering") {
        $ssid = ReadSettingFromFile("TetherSSID");
        $psk = ReadSettingFromFile("TetherPSK");
        if ($ssid == "") {
            $ssid = "FPP";
            WriteSettingToFile("TetherSSID", $ssid);
        }
        if ($psk == "") {
            $psk = "Christmas";
            WriteSettingToFile("TetherPSK", $psk);
        }
    } else if ($setting == "BBBLeds0" || $setting == "BBBLeds1" || $setting == "BBBLeds2" || $setting == "BBBLeds3" || $setting == "BBBLedPWR") {
        exec($SUDO . " " . $settings['fppDir'] . "/src/fppinit configureBBB", $output);
    } else if ($setting == "scheduling") {
        SendCommand("EnableScheduling,$value,");
    } else {
        ApplySetting($setting, $value);
        SendCommand("SetSetting,$setting,$value,");
    }

    //If the setting being set isn't the reboot or restart flag (we don't want to trigger a backup on these)
    if (in_array(ltrim(rtrim($setting)), array('restartFlag', 'rebootFlag', 'currentHeaderSensor')) === false) {
        //Make a call to the configuration backup API endpoint so we can generate a backup
        $backup_comment = GenerateBackupComment($setting, $value);
        GenerateBackupViaAPI($backup_comment);
    }

    $status = array("status" => "OK");
    return json($status);
}

/**
 * Get all settings.json
 *
 * Returns the `settings.json` metadata file as a JSON list of settings.
 *
 * @route GET /api/settings
