<p>The Script Repository is maintained separately from the main Falcon Player
source code.  These scripts may be installed individually using the Script
Repository Browser.  Downloaded scripts are installed automatically in
the FPP Scripts directory and may then be managed via the Scripts tab in
the <a href='uploadfile.php'>File Manager</a> UI.</p>

<p>You may be required to edit some scripts to make them functional.  For
example, the Remote Control example scripts need to be modified to include
the IP address of the remote FPP instance that you wish to control.</p>
